using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Example_710_Intro
{
    /// <summary>
    /// Структура, описывающая рабочего
    /// </summary>
    struct Worker
    {
        /// <summary>
        /// Должность
        /// </summary>
        public string Position;

        /// <summary>
        /// Зарплата
        /// </summary>
        public uint Salary;

        /// <summary>
        /// Имя
        /// </summary>
        public string FirstName;

        /// <summary>
        /// Фамилия
        /// </summary>
        public string LastName;

        /// <summary>
        /// Дата рождения
        /// </summary>
        public DateTime DateOfBirth;
    }
}
